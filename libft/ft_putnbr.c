#include "libft.h"
#include <unistd.h>

void	ft_putnbr(int n)
{
	long l;
	char c;

	l = n;
	if (l < 0)
	{
		write(1, "-", 1);
		l = -l;
	}
	if (l > 9)
		ft_putnbr(l / 10);
	c = l % 10 + '0';
	write(1, &c, 1);
}
