#include "libft.h"

int		ft_atoi(const char *nb)
{
	int		neg;
	long	res;
	int		i;

	neg = 1;
	res = 0;
	i = 0;
	while ((nb[i] == '\t') || (nb[i] == '\n') || (nb[i] == '\r')
			|| (nb[i] == '\v') || (nb[i] == ' ') || (nb[i] == '\f'))
		i++;
	if (nb[i] == '-')
		neg = -1;
	if ((nb[i] == '+') || (nb[i] == '-'))
		i++;
	while ((nb[i]) && (nb[i] >= '0') && (nb[i] <= '9'))
		res = (res * 10) + (nb[i++] - '0');
	return (neg * res);
}
